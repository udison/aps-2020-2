    private void btn_AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AlterarActionPerformed
      ativaCampos();
      
      this.btn_Salvar.setEnabled(true);
        
      updateID = Integer.parseInt(this.tb_Planta.getValueAt(this.tb_Planta.getSelectedRow(), 0).toString());

      try {
        PreparedStatement stmt = connection.prepareStatement("SELECT id, nome, nome_cientifico, dias, solo, densidade, espac_fileiras, temp_solo, prof_solo FROM planta WHERE planta.id = ?");
        stmt.setInt(1, updateID);
        ResultSet rs = stmt.executeQuery();
        
        Planta planta = new Planta();

        while(rs.next()) {
          planta = new Planta(
            rs.getInt("id"),
            rs.getString("nome"),
            rs.getString("nome_cientifico"),
            rs.getInt("dias"),
            rs.getString("solo"),
            rs.getFloat("densidade"),
            rs.getInt("espac_fileiras"),
            rs.getInt("temp_solo"),
            rs.getInt("prof_solo")
          );
          System.out.println(rs.getInt("temp_solo"));
        }

        txt_EspacamentoFileira.setText(Float.toString(planta.getEspacFileiras()));
        txt_NomeCientifico.setText(planta.getNomeCientifico());
        txt_NomePlanta.setText(planta.getNome());
        txt_Profundidade.setText(Integer.toString(planta.getProfSolo()));
        txt_QtdeDiasCrescimento.setText(Integer.toString(planta.getDias()));
        txt_QtdePlantas.setText(Float.toString(planta.getDensidade()));
        txt_Solo.setText(planta.getSolo());
        txt_temperatura.setText(Integer.toString(planta.getTempSolo()));
      } catch (Exception e) {
        JOptionPane.showMessageDialog(rootPane, "Erro: " + e.getMessage(), "Alerta", JOptionPane.ERROR_MESSAGE);
      }
    }//GEN-LAST:event_btn_AlterarActionPerformed

