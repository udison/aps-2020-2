  private void loadTablePlantas() {
    // Se a conexão com o banco de dados foi feita com sucesso
    if(connection != null) {
      
      // Limpa a grid
      DefaultTableModel dtm = (DefaultTableModel) this.tb_Planta.getModel();
      dtm.setRowCount(0);
      
      try {
        PreparedStatement stm = connection.prepareStatement("SELECT planta.id, planta.nome, planta.nome_cientifico, planta.dias, planta.solo FROM planta");
        ResultSet result = stm.executeQuery();

        JTableUtils.fillTable(result, this.tb_Planta);
      }
      catch(Exception e) {
        JOptionPane.showMessageDialog(rootPane, "Erro: " + e.getMessage(), "Alerta", JOptionPane.ERROR_MESSAGE);
      }
    }
  }

